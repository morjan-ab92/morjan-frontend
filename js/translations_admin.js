export function getStoredAdminLang() {
    return localStorage.getItem(ADMIN_LANG_KEY) ||
        localStorage.getItem('preferred_lang') ||
        localStorage.getItem('language') ||
        document.documentElement.lang ||
        'ar';
}

export function setStoredAdminLang(lang) {
    localStorage.setItem(ADMIN_LANG_KEY, lang);
    localStorage.setItem('preferred_lang', lang);
    localStorage.setItem('language', lang);
}

export function getAdminTranslation(lang, key) {
    const dictionary = adminTranslations[lang] || adminTranslations.ar;
    return dictionary[key] || adminTranslations.ar[key] || key;
}

export function applyAdminTranslations(lang = getStoredAdminLang()) {
    const dictionary = adminTranslations[lang] || adminTranslations.ar;

    document.documentElement.lang = lang;
    // Both Arabic and Hebrew use RTL
    const isRTL = lang === 'ar' || lang === 'he';
    document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
    document.body.dir = isRTL ? 'rtl' : 'ltr';
    document.body.classList.toggle('rtl', isRTL);
    document.body.classList.toggle('ltr', !isRTL);

    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.dataset.translate;
        if (dictionary[key]) {
            el.textContent = dictionary[key];
        }
    });

    document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
        const key = el.dataset.translatePlaceholder;
        if (dictionary[key]) {
            el.setAttribute('placeholder', dictionary[key]);
        }
    });

    document.querySelectorAll('[data-translate-value]').forEach(el => {
        const key = el.dataset.translateValue;
        if (dictionary[key]) {
            el.setAttribute('value', dictionary[key]);
        }
    });

    // Handle option elements inside select dropdowns
    document.querySelectorAll('select option[data-translate]').forEach(el => {
        const key = el.dataset.translate;
        if (dictionary[key]) {
            el.textContent = dictionary[key];
        }
    });
}
